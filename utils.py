# FAIRNESS ANALYSIS
# --------------------------------------------------------------------------
def fairness_by_group(df: pd.DataFrame, group_col: str, prediction_col: str = "Outcome") -> pd.DataFrame:
    """Compute simple fairness statistics (positive-prediction rate) across
    the groups of a demographic column.

    Args:
        df: DataFrame containing at least ``group_col`` and
            ``prediction_col``.
        group_col: Column defining the demographic groups (e.g. "Gender").
        prediction_col: Binary outcome/prediction column (1 = positive).

    Returns:
        A DataFrame with, per group: count, positive rate (%), and mean
        risk score, useful for spotting disparate impact.
    """
    grouped = (
        df.groupby(group_col)[prediction_col]
        .agg(["count", "mean"])
        .rename(columns={"count": "Count", "mean": "PositiveRate"})
    )
    grouped["PositiveRate"] = (grouped["PositiveRate"] * 100).round(2)
    return grouped.reset_index()


def age_group_bucket(age: float) -> str:
    """Bucket a numeric age into a coarse age-group label for fairness
    analysis."""
    if age < 30:
        return "18-29"
    if age < 45:
        return "30-44"
    if age < 60:
        return "45-59"
    return "60+"
