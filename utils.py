class DiabetesReportPDF(FPDF):
    """Custom FPDF subclass adding a consistent header/footer."""

    def header(self) -> None:  # noqa: D102 (fpdf hook)
        self.set_font("Helvetica", "B", 14)
        self.set_text_color(30, 30, 30)
        self.cell(0, 10, "Diabetes Risk Screening Report", ln=True, align="C")
        self.set_font("Helvetica", "", 9)
        self.set_text_color(120, 120, 120)
        self.cell(0, 6, "Generated by the Explainable AI Diabetes Risk System", ln=True, align="C")
        self.ln(4)

    def footer(self) -> None:  # noqa: D102 (fpdf hook)
        self.set_y(-15)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, f"Page {self.page_no()}", align="C")


def generate_pdf_report(
    patient: dict,
    risk_band: str,
    risk_score: float,
    confidence: float,
    reasoning_trace: list[str],
    recommendations: list[str],
) -> bytes:
    """Generate a PDF risk-screening report and return it as raw bytes.

    Args:
        patient: Raw patient input values.
        risk_band: Predicted risk category (e.g. "High Risk").
        risk_score: Risk probability, expressed as a percentage (0-100).
        confidence: Model confidence score (0-100).
        reasoning_trace: List of human-readable reasoning steps.
        recommendations: List of lifestyle/medical recommendations.

    Returns:
        The PDF file content as a ``bytes`` object, ready to be offered as
        a Streamlit download.
    """
    pdf = DiabetesReportPDF()
    pdf.add_page()

    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(0, 0, 0)
    pdf.cell(0, 8, f"Date: {datetime.now().strftime('%Y-%m-%d %H:%M')}", ln=True)
    pdf.ln(2)

    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(0, 8, "Prediction Summary", ln=True)
    pdf.set_font("Helvetica", "", 10)
    pdf.cell(0, 7, f"Risk Category: {risk_band}", ln=True)
    pdf.cell(0, 7, f"Risk Score: {risk_score:.1f}%", ln=True)
    pdf.cell(0, 7, f"Model Confidence: {confidence:.1f}%", ln=True)
    pdf.ln(3)

    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(0, 8, "Patient Inputs", ln=True)
    pdf.set_font("Helvetica", "", 10)
    for key, value in patient.items():
        pdf.cell(0, 6, f"{key}: {value}", ln=True)
    pdf.ln(3)

    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(0, 8, "Reasoning Trace", ln=True)
    pdf.set_font("Helvetica", "", 9)
    if reasoning_trace:
        for step in reasoning_trace:
            pdf.multi_cell(0, 5.5, f"- {step}")
    else:
        pdf.multi_cell(0, 5.5, "No additional symbolic rules fired for this patient.")
    pdf.ln(3)

    pdf.set_font("Helvetica", "B", 12)
    pdf.cell(0, 8, "Recommendations", ln=True)
    pdf.set_font("Helvetica", "", 10)
    for rec in recommendations:
        pdf.multi_cell(0, 6, f"- {rec}")
    pdf.ln(4)

    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(150, 0, 0)
    pdf.multi_cell(
        0,
        5,
        "Disclaimer: This report is generated by an automated screening tool "
        "for educational purposes only. It is NOT a medical diagnosis. "
        "Please consult a licensed healthcare professional for an accurate "
        "diagnosis and treatment plan.",
    )

    output = pdf.output(dest="S")
    if isinstance(output, str):
        return output.encode("latin-1")
    return bytes(output)


